package ejecutarhumano;

/**
 *
 * @author ASUS
 */
public class Ropa {
    private String idRopa;
    private String colorRopa;
    private String tipo;
    private String marca;
    private double talla;

    public String getIdRopa() {
        return idRopa;
    }

    public void setIdRopa(String idRopa) {
        this.idRopa = idRopa;
    }

    public String getColorRopa() {
        return colorRopa;
    }

    public void setColorRopa(String colorRopa) {
        this.colorRopa = colorRopa;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public double getTalla() {
        return talla;
    }

    public void setTalla(double talla) {
        this.talla = talla;
    }
           
}
